package go.g2academy.Day5;

public class ForIncrementDemo {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        System.out.println("\n");

        for (int i = 0; i < 10; ++i) {
            System.out.println(i);
        }
    }
}
