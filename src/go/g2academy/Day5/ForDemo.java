package go.g2academy.Day5;

public class ForDemo {
    public static void main(String[] args) {
        for (int i=1; i<11; i++){
            System.out.println("Count is: " + i);
        }

        System.out.println("\n");

        for (int i=1; i<11; i+=2){
            System.out.println("Count is: " + i);
        }
    }
}
