package go.g2academy.Day5;

public class UnaryOperator {
    public static void main(String[] args) {
        int n = 10;
        System.out.println(n--);
        System.out.println(n);

        System.out.println("\n");

        int x = 10;
        System.out.println(--x);
        System.out.println(x);
    }
}
